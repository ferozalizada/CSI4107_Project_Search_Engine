    def spell_checker(self, words, candidates=False):
        misspelled = words.split()
        input_words = self.__spell_check.unknown(misspelled)
        # print('',input_words)
        corrected_words = []
        for word in input_words:
            # if word == "": continue
            if(not candidates):
                # print('jere')
                corrected_words.append(self.__spell_check.correction(word))
            else:
                corrected_words.append(self.__spell_check.candidates(word))

        return corrected_words

    def spell_checker_sentence(self, words, candidates=False):

        return self.__spell_check.unknown(words.split())

